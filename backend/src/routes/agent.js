const express = require('express');
const { requireAuth, requireAdmin } = require('../middleware/authMiddleware');

const router = express.Router();
const REGISTRATION_KEY = process.env.REGISTRATION_KEY || 'winserv-reg-key-change-me';
const AGENT_VERSION = '2.9';

function generateUniversalScript(serverUrl, regKey) {
  return [
    '# WinServ Monitoring Agent v' + AGENT_VERSION,
    '# ====================================================================',
    '# Auto-registers on first run. One script for all servers.',
    '# Save to C:\\winserv-agent\\agent.ps1 and schedule:',
    '#   schtasks /create /tn "WinServAgent" /ru SYSTEM /rl HIGHEST /sc minute /mo 1 /tr "C:\\Windows\\System32\\WindowsPowerShell\\v1.0\\powershell.exe -ExecutionPolicy Bypass -NoProfile -WindowStyle Hidden -File C:\\winserv-agent\\agent.ps1" /f',
    '# ====================================================================',
    'try { [Console]::OutputEncoding = [System.Text.Encoding]::UTF8 } catch {}',
    '[System.Net.ServicePointManager]::SecurityProtocol = [System.Net.SecurityProtocolType]::Tls12 -bor [System.Net.SecurityProtocolType]::Tls13',
    '',
    '$ErrorActionPreference = "Continue"',
    '$AgentVersion = "' + AGENT_VERSION + '"',
    '$ServerUrl = "' + serverUrl + '"',
    '$MetricsUrl = "$ServerUrl/api/metrics"',
    '$EventsUrl = "$ServerUrl/api/events"',
    '$SecurityUrl = "$ServerUrl/api/security"',
    '$HealthUrl = "$ServerUrl/api/health-report"',
    '$RegKey = "' + regKey + '"',
    '$ConfigFile = "$env:ProgramData\\WinServAgent\\config.json"',
    '$LogFile = "$env:ProgramData\\WinServAgent\\agent.log"',
    '',
    '# Heartbeat: write timestamp to prove script started',
    'try { Get-Date -Format \'yyyy-MM-dd HH:mm:ss\' | Out-File "C:\\winserv-agent\\heartbeat.txt" -Encoding UTF8 } catch {}',
    '',
    'function Write-Log($msg) {',
    '  $dir = Split-Path $LogFile -Parent',
    '  if (-not (Test-Path $dir)) { New-Item -ItemType Directory -Path $dir -Force | Out-Null }',
    '  $ts = Get-Date -Format \'yyyy-MM-dd HH:mm:ss\'',
    '  "$ts $msg" | Out-File $LogFile -Append -Encoding UTF8',
    '}',
    '',
    'Write-Log "Agent started"',
    '',
    '$FullHostname = try { [System.Net.Dns]::GetHostEntry(\'\').HostName } catch { "$env:COMPUTERNAME.$env:USERDNSDOMAIN" }',
    'if (-not $FullHostname -or $FullHostname -notmatch \'\\.\') { $FullHostname = "$env:COMPUTERNAME.$env:USERDNSDOMAIN" }',
    '',
    'function Get-SystemMetrics {',
    '  $cpu = (Get-CimInstance Win32_Processor | Measure-Object -Property LoadPercentage -Average).Average',
    '  $os = Get-CimInstance Win32_OperatingSystem',
    '  $memTotalMB = [math]::Round($os.TotalVisibleMemorySize / 1024, 0)',
    '  $memFreeMB = [math]::Round($os.FreePhysicalMemory / 1024, 0)',
    '  $memUsedMB = $memTotalMB - $memFreeMB',
    '  $disks = Get-CimInstance Win32_LogicalDisk -Filter "DriveType=3" -ErrorAction SilentlyContinue',
    '  $diskPerf = @{}',
    '  try {',
    '    Get-CimInstance Win32_PerfFormattedData_PerfDisk_LogicalDisk -ErrorAction Stop | Where-Object { $_.Name -match \'^[A-Z]:$\' } | ForEach-Object {',
    '      $diskPerf[$_.Name] = @{',
    '        read_bytes_sec = [math]::Round($_.DiskReadBytesPerSec, 0)',
    '        write_bytes_sec = [math]::Round($_.DiskWriteBytesPerSec, 0)',
    '        disk_time_pct = [math]::Round($_.PercentDiskTime, 1)',
    '        queue_length = [math]::Round($_.CurrentDiskQueueLength, 1)',
    '      }',
    '    }',
    '  } catch {}',
    '  $diskArray = @()',
    '  $diskTotalGB = 0; $diskUsedGB = 0; $diskFreeGB = 0',
    '  foreach ($d in $disks) {',
    '    $sizeGB = [math]::Round($d.Size / 1GB, 1)',
    '    $freeGB = [math]::Round($d.FreeSpace / 1GB, 1)',
    '    $usedGB = [math]::Round(($d.Size - $d.FreeSpace) / 1GB, 1)',
    '    $diskTotalGB += $sizeGB',
    '    $diskFreeGB += $freeGB',
    '    $diskUsedGB += $usedGB',
    '    $perf = $diskPerf[$d.DeviceID]',
    '    $diskEntry = @{ drive = $d.DeviceID; total_gb = $sizeGB; used_gb = $usedGB; free_gb = $freeGB }',
    '    if ($perf) {',
    '      $diskEntry.read_bytes_sec = $perf.read_bytes_sec',
    '      $diskEntry.write_bytes_sec = $perf.write_bytes_sec',
    '      $diskEntry.disk_time_pct = $perf.disk_time_pct',
    '      $diskEntry.queue_length = $perf.queue_length',
    '    }',
    '    $diskArray += $diskEntry',
    '  }',
    '  $uptime = [math]::Round(((Get-Date) - $os.LastBootUpTime).TotalSeconds, 0)',
    '  return @{',
    '    cpu_usage = $cpu',
    '    memory_total_mb = $memTotalMB',
    '    memory_used_mb = $memUsedMB',
    '    disk_total_gb = $diskTotalGB',
    '    disk_used_gb = $diskUsedGB',
    '    disk_free_gb = $diskFreeGB',
    '    disks = $diskArray',
    '    uptime_seconds = $uptime',
    '  }',
    '}',
    '',
    'function Get-CriticalEvents {',
    '  $since = (Get-Date).AddMinutes(-30)',
    '  $events = $null',
    '  try {',
    '    $events = Get-WinEvent -FilterHashtable @{LogName=\'System\';Level=1,2,3;StartTime=$since} -MaxEvents 200 -ErrorAction Stop',
    '  } catch {',
    '    try {',
    '      $events = Get-EventLog -LogName System -EntryType Error,Warning -After $since -Newest 200 -ErrorAction Stop',
    '    } catch {',
    '      Write-Log "EventLog: cannot read System log"',
    '      return @()',
    '    }',
    '  }',
    '  if (-not $events) { return @() }',
    '  $result = @()',
    '  foreach ($e in $events) {',
    '    $source = if ($e.ProviderName) { $e.ProviderName } else { $e.Source }',
    '    $eid = if ($e.Id) { $e.Id } else { $e.EventID }',
    '    $lvlNum = if ($e.Level) { $e.Level } else { 2 }',
    '    if ($lvlNum -le 1) { $lvl = \'Critical\' }',
    '    elseif ($lvlNum -le 2) { $lvl = \'Error\' }',
    '    elseif ($lvlNum -le 3) { $lvl = \'Warning\' }',
    '    else { $lvl = \'Information\' }',
    '    try {',
    '      $msg = if ($e.Message) { if ($e.Message.Length -gt 2000) { $e.Message.Substring(0, 2000) } else { $e.Message } } else { \'\' }',
    '    } catch { $msg = \'\' }',
    '    $time = if ($e.TimeCreated) { $e.TimeCreated.ToString(\'yyyy-MM-ddTHH:mm:ss\') } else { $e.TimeGenerated.ToString(\'yyyy-MM-ddTHH:mm:ss\') }',
    '    $result += @{source=$source;event_id=$eid;level=$lvl;message=$msg;recorded_at=$time}',
    '  }',
    '  Write-Log "Events collected: $($result.Count)"',
    '  return $result',
    '}',
    '',
    'function Save-Token {',
    '  try {',
    '    $dir = Split-Path $ConfigFile -Parent',
    '    if (-not (Test-Path $dir)) { New-Item -ItemType Directory -Path $dir -Force | Out-Null }',
    '    @{token=$Token} | ConvertTo-Json | Set-Content $ConfigFile -Force',
    '  } catch { Write-Log "Save-Token error: $_" }',
    '}',
    '',
    'function Send-Body($url, $body) {',
    '  try {',
    '    return Invoke-RestMethod -Uri $url -Method POST -Body $body -ContentType "application/json; charset=utf-8" -TimeoutSec 15',
    '  } catch {',
    '    Write-Log "Send-Body $url : $_"',
    '    return $null',
    '  }',
    '}',
    '',
    'function Get-SecurityEvents {',
    '  # 4625 failed logons (brute-force signal) + 4624 RDP successes (type 10).',
    '  $since = (Get-Date).AddMinutes(-30)',
    '  try {',
    '    $events = Get-WinEvent -FilterHashtable @{LogName=\'Security\';Id=4624,4625;StartTime=$since} -MaxEvents 150 -ErrorAction Stop',
    '  } catch { return @() }',
    '  if (-not $events) { return @() }',
    '  $result = @()',
    '  foreach ($e in $events) {',
    '    try {',
    '      $x = [xml]$e.ToXml(); $d = @{}',
    '      foreach ($n in $x.Event.EventData.Data) { $d[$n.Name] = $n."#text" }',
    '      $lt = "$($d[\'LogonType\'])"',
    '      $ip = "$($d[\'IpAddress\'])"',
    '      $acct = "$($d[\'TargetUserName\'])"',
    '      if ($e.Id -eq 4625) {',
    '        if (-not $ip -or $ip -eq \'-\' -or $ip -eq \'::1\' -or $ip -eq \'127.0.0.1\') { continue }',
    '        $result += @{event=\'fail\';account=$acct;ip=$ip;logon_type=$lt;recorded_at=$e.TimeCreated.ToString(\'yyyy-MM-ddTHH:mm:ss\')}',
    '      } else {',
    '        if ($lt -ne \'10\') { continue }',
    '        $result += @{event=\'success\';account=$acct;ip=$ip;logon_type=$lt;recorded_at=$e.TimeCreated.ToString(\'yyyy-MM-ddTHH:mm:ss\')}',
    '      }',
    '    } catch {}',
    '  }',
    '  return $result',
    '}',
    '',
    'function Get-HealthInfo {',
    '  $h = @{ pending_reboot = $false; services = @(); certs = @(); tasks = @() }',
    '  try {',
    '    Get-CimInstance Win32_Service -Filter "StartMode=\'Auto\' AND State=\'Stopped\'" -ErrorAction Stop | ForEach-Object {',
    '      $h.services += @{ name = $_.Name; display = $_.DisplayName }',
    '    }',
    '  } catch {}',
    '  try {',
    '    if (Test-Path "HKLM:\\SOFTWARE\\Microsoft\\Windows\\CurrentVersion\\Component Based Servicing\\RebootPending") { $h.pending_reboot = $true }',
    '    if (Test-Path "HKLM:\\SOFTWARE\\Microsoft\\Windows\\CurrentVersion\\WindowsUpdate\\Auto Update\\RebootRequired") { $h.pending_reboot = $true }',
    '    $pfr = (Get-ItemProperty "HKLM:\\SYSTEM\\CurrentControlSet\\Control\\Session Manager" -Name PendingFileRenameOperations -ErrorAction SilentlyContinue)',
    '    if ($pfr -and $pfr.PendingFileRenameOperations) { $h.pending_reboot = $true }',
    '  } catch {}',
    '  try {',
    '    $limit = (Get-Date).AddDays(30)',
    '    Get-ChildItem Cert:\\LocalMachine\\My -ErrorAction Stop | Where-Object { $_.NotAfter -lt $limit -and $_.NotAfter -gt (Get-Date).AddDays(-1) } | ForEach-Object {',
    '      $h.certs += @{ subject = $_.Subject; expires = $_.NotAfter.ToString(\'yyyy-MM-dd\') }',
    '    }',
    '  } catch {}',
    '  try {',
    '    Get-ScheduledTask -ErrorAction Stop | Where-Object { $_.State -ne "Disabled" } | ForEach-Object {',
    '      $info = $_ | Get-ScheduledTaskInfo -ErrorAction SilentlyContinue',
    '      if ($info -and $info.LastTaskResult -ne 0 -and $info.LastTaskResult -ne 267009 -and $info.LastTaskResult -ne 267011 -and $info.LastRunTime -gt (Get-Date).AddDays(-2)) {',
    '        $h.tasks += @{ name = $_.TaskName; result = $info.LastTaskResult }',
    '      }',
    '    }',
    '  } catch {}',
    '  return $h',
    '}',
    '',
    '# --- Main execution ---',
    'try {',
    '  Write-Log "Starting collection"',
    '',
    '  try { $osInfo = (Get-CimInstance Win32_OperatingSystem).Caption } catch { $osInfo = "Windows" }',
    '  Write-Log "OS: $osInfo"',
    '',
    '  try {',
    '    $ip = (Get-NetIPAddress -AddressFamily IPv4 -PrefixOrigin Dhcp -ErrorAction SilentlyContinue | Select-Object -First 1).IPAddress',
    '    if (-not $ip) { $ip = (Get-NetIPAddress -AddressFamily IPv4 | Where-Object { $_.InterfaceAlias -notmatch \'Loopback\' } | Select-Object -First 1).IPAddress }',
    '  } catch { $ip = "" }',
    '  Write-Log "IP: $ip"',
    '',
    '  $Token = $null',
    '  if (Test-Path $ConfigFile) {',
    '    try { $config = Get-Content $ConfigFile -Raw | ConvertFrom-Json; $Token = $config.token } catch {}',
    '  }',
    '  Write-Log "Token loaded: $($Token -ne $null)"',
    '',
    '  try { $metricsObj = Get-SystemMetrics } catch { Write-Log "Metrics error: $_"; $metricsObj = $null }',
    '  Write-Log "Metrics collected: $($metricsObj -ne $null)"',
    '',
    '  if ($metricsObj) {',
    '    if ($Token) {',
    '      $body = @{token=$Token;hostname=$FullHostname;ip_address=$ip;os_info=$osInfo;agent_version=$AgentVersion;metrics=$metricsObj} | ConvertTo-Json -Depth 6',
    '      $response = Send-Body $MetricsUrl $body',
    '      if ($response) {',
    '        if ($response.token) { $Token = $response.token; Save-Token }',
    '        $lastResponse = $response',
    '        Write-Log "Metrics sent with token"',
    '      } else {',
    '        $Token = $null',
    '        Write-Log "Metrics failed, will re-register"',
    '      }',
    '    }',
    '',
    '    if (-not $Token) {',
    '      $body = @{registration_key=$RegKey;hostname=$FullHostname;ip_address=$ip;os_info=$osInfo;agent_version=$AgentVersion;metrics=$metricsObj} | ConvertTo-Json -Depth 6',
    '      $response = Send-Body $MetricsUrl $body',
    '      if ($response -and $response.token) {',
    '        $Token = $response.token; Save-Token',
    '        $lastResponse = $response',
    '        Write-Log "Registered: $FullHostname"',
    '      } else {',
    '        Write-Log "Registration failed"',
    '      }',
    '    }',
    '  }',
    '',
    '  try { $events = Get-CriticalEvents } catch { Write-Log "Events collection error: $_"; $events = @() }',
    '  Write-Log "Events found: $($events.Count)"',
    '',
    '  if ($events.Count -gt 0) {',
    '    if ($Token) {',
    '      $eventsBody = @{token=$Token;hostname=$FullHostname;events=$events} | ConvertTo-Json -Depth 6',
    '      $r = Send-Body $EventsUrl $eventsBody',
    '      if ($r) { Write-Log "Events sent: $($events.Count)" }',
    '    } else {',
    '      $eventsBody = @{registration_key=$RegKey;hostname=$FullHostname;events=$events} | ConvertTo-Json -Depth 6',
    '      $r = Send-Body $EventsUrl $eventsBody',
    '      if ($r) { Write-Log "Events sent (reg): $($events.Count)" }',
    '    }',
    '  }',
    '',
    '  # Heavy collectors are throttled so per-minute runs stay fast and never',
    '  # block the metrics cadence (busy servers were stalling ~6 min otherwise).',
    '  $minNow = (Get-Date).Minute',
    '',
    '  # --- Security logons (4625/4624) — every 5 minutes ---',
    '  if ($Token -and ($minNow % 5) -eq 0) {',
    '    try { $secEvents = Get-SecurityEvents } catch { Write-Log "Security collection error: $_"; $secEvents = @() }',
    '    Write-Log "Security events found: $($secEvents.Count)"',
    '    if ($secEvents.Count -gt 0) {',
    '      $secBody = @{token=$Token;hostname=$FullHostname;events=$secEvents} | ConvertTo-Json -Depth 6',
    '      $r = Send-Body $SecurityUrl $secBody',
    '      if ($r) { Write-Log "Security sent: $($secEvents.Count)" }',
    '    }',
    '  }',
    '',
    '  # --- Deep health (services, reboot, certs, tasks) — every 10 minutes ---',
    '  if ($Token -and ($minNow % 10) -eq 0) {',
    '    try {',
    '      $health = Get-HealthInfo',
    '      $hBody = @{token=$Token;hostname=$FullHostname;pending_reboot=$health.pending_reboot;services=$health.services;certs=$health.certs;tasks=$health.tasks} | ConvertTo-Json -Depth 6',
    '      $r = Send-Body $HealthUrl $hBody',
    '      if ($r) { Write-Log "Health sent: svc=$($health.services.Count) certs=$($health.certs.Count) tasks=$($health.tasks.Count)" }',
    '    } catch { Write-Log "Health error: $_" }',
    '  }',
    '',
    '  # --- Process server actions (file rename + user logout) ---',
    '  if ($lastResponse -and $lastResponse.actions) {',
    '    foreach ($act in $lastResponse.actions) {',
    '      if ($act.applied -ne 0) { continue }',
    '      try {',
    '        $targetPath = $act.file_path',
    '        $hiddenPath = Join-Path (Split-Path $targetPath -Parent) ("~" + (Split-Path $targetPath -Leaf) + ".old")',
    '        if ($act.hidden -eq 1) {',
    '          if (Test-Path $targetPath) {',
    '            Move-Item $targetPath $hiddenPath -Force',
    '            Write-Log "Action: hidden $targetPath"',
    '          }',
    '          if ($act.logout_users -eq 1) {',
    '            Write-Log "Starting force logoff..."',
    '            for ($id = 1; $id -le 200; $id++) {',
    '              & C:\\Windows\\System32\\logoff.exe $id 2>&1 | Out-Null',
    '            }',
    '            Write-Log "Force logoff complete"',
    '          }',
    '        } else {',
    '          if (Test-Path $hiddenPath) {',
    '            Move-Item $hiddenPath $targetPath -Force',
    '            Write-Log "Action: restored $targetPath"',
    '          }',
    '        }',
    '        $reportBody = @{token=$Token; action_id=$act.id; success=$true} | ConvertTo-Json -Compress',
    '        try { Invoke-RestMethod -Uri "$ServerUrl/api/actions/$($act.id)/report" -Method POST -Body $reportBody -ContentType "application/json; charset=utf-8" -TimeoutSec 10 } catch {}',
    '      } catch { Write-Log "Action error: $_" }',
    '    }',
    '  }',
    '',
    '  # --- Process one-shot commands (restart service / reboot) ---',
    '  if ($lastResponse -and $lastResponse.commands) {',
    '    foreach ($cmd in $lastResponse.commands) {',
    '      $ok = $false; $msg = ""',
    '      try {',
    '        if ($cmd.ctype -eq "restart_service") {',
    '          Restart-Service -Name $cmd.param -Force -ErrorAction Stop',
    '          $ok = $true; $msg = "restarted $($cmd.param)"',
    '          Write-Log "Command: restarted service $($cmd.param)"',
    '        } elseif ($cmd.ctype -eq "block_ip") {',
    '          $rule = "WinServ Block $($cmd.param)"',
    '          if (-not (Get-NetFirewallRule -DisplayName $rule -ErrorAction SilentlyContinue)) {',
    '            New-NetFirewallRule -DisplayName $rule -Direction Inbound -RemoteAddress $cmd.param -Action Block -Profile Any -ErrorAction Stop | Out-Null',
    '          }',
    '          $ok = $true; $msg = "blocked $($cmd.param)"',
    '          Write-Log "Command: blocked IP $($cmd.param)"',
    '        } elseif ($cmd.ctype -eq "reboot") {',
    '          $ok = $true; $msg = "rebooting"',
    '          Write-Log "Command: reboot requested"',
    '        } elseif ($cmd.ctype -eq "uninstall_agent") {',
    '          $ok = $true; $msg = "uninstalling"',
    '          Write-Log "Command: uninstall requested"',
    '        }',
    '      } catch { $ok = $false; $msg = "$_"; Write-Log "Command error: $_" }',
    '      $rep = @{token=$Token; success=$ok; result=$msg} | ConvertTo-Json -Compress',
    '      try { Invoke-RestMethod -Uri "$ServerUrl/api/commands/$($cmd.id)/report" -Method POST -Body $rep -ContentType "application/json; charset=utf-8" -TimeoutSec 10 } catch {}',
    '      if ($cmd.ctype -eq "reboot" -and $ok) { Start-Sleep -Seconds 2; Restart-Computer -Force }',
    '      # Remove the scheduled task and agent files after reporting success.',
    '      if ($cmd.ctype -eq "uninstall_agent" -and $ok) {',
    '        try { schtasks /delete /tn "WinServAgent" /f 2>$null | Out-Null } catch {}',
    '        Remove-Item -Recurse -Force "$env:ProgramData\\WinServAgent" -ErrorAction SilentlyContinue',
    '        Start-Process -FilePath "cmd.exe" -ArgumentList \'/c timeout /t 5 & rmdir /s /q C:\\winserv-agent\' -WindowStyle Hidden',
    '        return',
    '      }',
    '    }',
    '  }',
    '',
    '  Write-Log "Agent completed successfully"',
    '} catch {',
    '  Write-Log "FATAL: $_"',
    '} finally {',
    '  # Self-update runs in finally so a failure in the collection above (e.g. on a',
    '  # busy DC/Exchange) can never skip it. Writes to a temp file, then atomically',
    '  # renames into place — safer than an in-place overwrite of the running script.',
    '  if ($lastResponse -and $lastResponse.agent_latest -and $lastResponse.agent_latest -ne $AgentVersion -and $Token) {',
    '    $selfPath = $PSCommandPath; if (-not $selfPath) { $selfPath = "C:\\winserv-agent\\agent.ps1" }',
    '    Write-Log "Self-update: $AgentVersion -> $($lastResponse.agent_latest) target=$selfPath"',
    '    try {',
    '      $new = Invoke-RestMethod -Uri "$ServerUrl/api/agent/self-update?token=$Token" -Method GET -TimeoutSec 30',
    '      if ($new -and $new.Length -gt 1000 -and $new -match "WinServ Monitoring Agent") {',
    '        $tmp = "$selfPath.new"',
    '        [System.IO.File]::WriteAllText($tmp, $new, (New-Object System.Text.UTF8Encoding($false)))',
    '        Move-Item -Path $tmp -Destination $selfPath -Force',
    '        Write-Log "Self-updated OK -> $($lastResponse.agent_latest)"',
    '      } else { Write-Log "Self-update rejected payload len=$($new.Length)" }',
    '    } catch { Write-Log "Self-update failed: $_" }',
    '  }',
    '}',
  ].join('\n');
}

router.get('/script', requireAuth, requireAdmin, async (req, res) => {
  const serverUrl = (process.env.PUBLIC_URL || 'http://localhost:' + (process.env.PORT || '3000')).replace(/\/$/, '');
  res.type('text/plain; charset=utf-8');
  res.send(generateUniversalScript(serverUrl, REGISTRATION_KEY));
});

router.get('/script/:serverId', requireAuth, requireAdmin, async (req, res) => {
  const serverUrl = (process.env.PUBLIC_URL || 'http://localhost:' + (process.env.PORT || '3000')).replace(/\/$/, '');
  res.type('text/plain; charset=utf-8');
  res.send(generateUniversalScript(serverUrl, REGISTRATION_KEY));
});

// Agent self-update: returns the latest script to an agent presenting a valid
// token. The reg key it embeds is already baked into that agent, so this
// exposes nothing new.
const db = require('../db');
router.get('/self-update', async (req, res) => {
  const token = req.query.token;
  if (!token) return res.status(401).type('text/plain').send('# token required');
  const agent = await db.queryOne(
    'SELECT at.server_id, s.hostname, s.agent_version FROM agent_tokens at JOIN servers s ON s.id = at.server_id WHERE at.token = $1',
    [token]
  );
  if (!agent) return res.status(401).type('text/plain').send('# invalid token');
  console.log(`[Self-update] served to ${agent.hostname} (was v${agent.agent_version || '?'} -> v${AGENT_VERSION})`);
  const serverUrl = (process.env.PUBLIC_URL || 'http://localhost:' + (process.env.PORT || '3000')).replace(/\/$/, '');
  res.type('text/plain; charset=utf-8');
  res.send(generateUniversalScript(serverUrl, REGISTRATION_KEY));
});

// Latest agent version, so the UI can flag outdated hosts.
router.get('/version', requireAuth, (req, res) => res.json({ latest: AGENT_VERSION }));

